<?php
// Usando un bucle for
for ($i = 1; $i <= 10; $i++) {
    echo $i . "<br>"; // Imprime el número seguido de un salto de línea
}

// Usando un bucle while
$i = 1; // Inicializar la variable
while ($i <= 10) {
    echo $i . "<br>"; // Imprime el número seguido de un salto de línea
    $i++; // Incrementar la variable
}
?>